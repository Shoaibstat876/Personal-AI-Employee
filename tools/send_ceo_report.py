import json
import os
from datetime import datetime, timezone
from pathlib import Path

import resend
from dotenv import load_dotenv

load_dotenv()

PROJECT = Path(r"D:\Shoaib Project\Personal-AI-Employee")
VAULT = PROJECT / "AI_Employee_Vault"
LOGS = VAULT / "Logs"
BRIEFINGS = VAULT / "Briefings"
EVIDENCE_SCHEDULING = PROJECT / "evidence" / "gold" / "scheduling"

LOGS.mkdir(parents=True, exist_ok=True)
BRIEFINGS.mkdir(parents=True, exist_ok=True)
EVIDENCE_SCHEDULING.mkdir(parents=True, exist_ok=True)

resend.api_key = os.getenv("RESEND_API_KEY")


def iso_now_utc() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def local_now_display() -> str:
    return datetime.now().strftime("%Y-%m-%d %H:%M:%S")


def safe_env(name: str, default: str = "") -> str:
    value = os.getenv(name, default)
    return value.strip() if isinstance(value, str) else default


def write_briefing(ts_utc: str, now_local: str) -> Path:
    filename = f"CEO_BRIEFING_{ts_utc.replace(':', '').replace('-', '')}.md"
    briefing_path = BRIEFINGS / filename

    content = f"""---
generated_at_utc: "{ts_utc}"
report_type: "ceo_briefing"
source: "send_ceo_report.py"
status: "generated"
---

# Personal AI Employee — CEO Briefing

## System Status
- Status: Running
- Report Time: {now_local}
- Project: Personal AI Employee
- Tier: Gold Development

## MCP Servers
- Filesystem MCP: Active
- GitHub MCP: Active
- Slack MCP: Active
- Google Calendar MCP: Active

## Automation Systems
- Email Automation: Working
- Odoo Automation: Active
- Audit Logging: Enabled

## Recent Achievements
- Custom domain email infrastructure configured
- Resend API successfully integrated
- Automated system notification email tested

## Operator Note
This briefing was automatically generated by the Personal AI Employee system.
"""
    briefing_path.write_text(content, encoding="utf-8")
    return briefing_path


