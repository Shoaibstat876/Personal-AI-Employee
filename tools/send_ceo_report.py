def append_log(ts_utc: str, briefing_path: Path, email_result) -> Path:
    log_path = LOGS / "ceo_report_log.jsonl"

    entry = {
        "ts": ts_utc,
        "event": "CEO_REPORT_SENT",
        "briefing_file": briefing_path.name,
        "briefing_path": str(briefing_path),
        "email_to": safe_env("TO_EMAIL"),
        "email_from": safe_env("FROM_EMAIL"),
        "provider": "resend",
        "result": str(email_result),
    }

    with log_path.open("a", encoding="utf-8") as f:
        f.write(json.dumps(entry, ensure_ascii=False) + "\n")

    return log_path


def main() -> None:
    from_email = safe_env("FROM_EMAIL")
    to_email = safe_env("TO_EMAIL")

    if not resend.api_key:
        raise SystemExit("STOP: RESEND_API_KEY is missing in environment.")
    if not from_email:
        raise SystemExit("STOP: FROM_EMAIL is missing in environment.")
    if not to_email:
        raise SystemExit("STOP: TO_EMAIL is missing in environment.")

    ts_utc = iso_now_utc()
    now_local = local_now_display()

    briefing_path = write_briefing(ts_utc, now_local)
    report_html = build_email_html(now_local, briefing_path)

    params = {
        "from": from_email,
        "to": [to_email],
        "subject": "CEO System Report - Personal AI Employee",
        "html": report_html,
    }

    email_result = resend.Emails.send(params)
    log_path = append_log(ts_utc, briefing_path, email_result)

    print(f"[OK] generated briefing: {briefing_path}")
    print(f"[OK] email result: {email_result}")
    print(f"[OK] log updated: {log_path}")


if __name__ == "__main__":
    main()